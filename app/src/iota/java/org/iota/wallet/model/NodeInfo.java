package org.iota.wallet.model;

public class NodeInfo {

    private String param;
    private String value;
    private int index;

    public NodeInfo(String param, String value) {
        this.param = param;
        this.value = value;
    }

    public NodeInfo(String param, int index) {
        this.param = param;
        this.index = index;
    }

    public String getParam() {
        return param;
    }

    public void setParam(String param) {
        this.param = param;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }
}
